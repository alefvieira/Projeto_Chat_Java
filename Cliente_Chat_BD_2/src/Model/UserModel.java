public class UserModel {
    private String nome;
    private String user;
    private String senha; 

    public void cadastroUserModel(String n,String u, String s){
        this.nome = n;
        this.user = u;
        this.senha = s;
    }
    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
    
}
